import type { Rarity, GearSlot } from "./types";
import type { AttrGroup } from "./attributeData";

// ─────────────────────────────────────────────────────────────────────────────
// Slot type primitives
// ─────────────────────────────────────────────────────────────────────────────

/** A core attribute slot on a gear piece */
export type CoreSlot =
  | { type: "any-standard" }
  | { type: "fixed"; name: string };

/** A minor attribute slot on a gear piece */
export type MinorSlot =
  | { type: "fixed"; name: string; value: string }
  | { type: "any-basic" }
  | { type: "any-basic-restricted"; exclude: string[] }
  | { type: "any-basic-group"; group: AttrGroup };

/**
 * A talent slot — only chest and backpack pieces have one.
 * Named items carry a fixed perfect talent; high-end pieces roll any basic talent;
 * exotic pieces carry an exclusive exotic talent. A small number of named pieces
 * allow any basic talent.
 */
export type TalentSlot =
  | { type: "fixed"; talentId: string }
  | { type: "any-basic" };

// ─────────────────────────────────────────────────────────────────────────────
// Named / Exotic / High-End gear item
// ─────────────────────────────────────────────────────────────────────────────

export interface NamedGearItem {
  gearId:         string;         // kebab-case unique ID
  gearName:       string;
  slot:           GearSlot;
  brand:          string;         // matches BrandDef.name or GearSetDef.name
  rarity:         Rarity;
  /** Fixed perk description present on named and exotic items */
  namedPerkDesc?: string;
  /** Talent slot — chest and backpack pieces only */
  talent?:        TalentSlot;
  /** 1–3 core attribute slots (Ninjabike Messenger Backpack has all 3 simultaneously) */
  cores:          CoreSlot[];
  /** 0–3 minor attribute slots */
  minors:         MinorSlot[];
  /** Number of gear mod slots (0–2); only mask, backpack, and chest can have mod slots */
  modSlots:       number;
  source:         string;
  flavorText?:    string;
}

